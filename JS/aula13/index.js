const nome = 'Vinícius Pitaméia Falcão'

console.log(nome.length) // quantia de caracteres na variavel

console.log(nome.split(' ')) // sepera as palavras pelo espaço

console.log(nome.indexOf('i')) // seleciona o primeiro indice da letra 'i'

console.log(nome.lastIndexOf('i')) // seleciona o último indice da letra 'i'

console.log(nome.toUpperCase()) // Todas letras maiúsculas

console.log(nome.toLowerCase()) // Todas letras minúscula

console.log(nome.slice(-5)) // corta a variavel